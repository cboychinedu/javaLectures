public class whileLoops {
    // Running the main function 
    public static void main(String[] args) {
        // Setting the count 
        int count = 0;
        int stop = 100;  

        // Executing the while loop 
        while (count <= stop) {
            // Display the count 
            System.out.printf("Counting %d", count).println(); 

            // Increment 
            count++; 
        }
    }
}