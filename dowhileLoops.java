public class dowhileLoops {
    // Runing the main function 
    public static void main(String[] args) {
        // Setting the count value to zero 
        int count = 0; 

        // do-while loop 
        do { 
            // Setting the count 
            System.out.println("Counting " + count); 

            // increment 
            count++; 

          // While loop   
        } while (count <= 20);
    }
}